async function populateLocations() {
  const client = await pool.connect();

  try {
    console.log('🚀 Starting location data population...');

    await client.query('BEGIN');

    // Insert location data
    for (const [country, cities] of Object.entries(LOCATION_DATA)) {
      for (const [city, areas] of Object.entries(cities)) {
        for (const [area, streets] of Object.entries(areas)) {
          for (const street of streets) {
            await client.query(
              'INSERT INTO locations (country, city, area, street) VALUES ($1, $2, $3, $4) ON CONFLICT DO NOTHING',
              [country, city, area, street]
            );
          }
        }
      }
    }

    // Insert coordinate mappings
    for (const [locationKey, mapping] of Object.entries(COORDINATE_MAPPINGS)) {
      await client.query(
        `INSERT INTO coordinate_mappings (location_key, country, city, lat_min, lat_max, lng_min, lng_max)
         VALUES ($1, $2, $3, $4, $5, $6, $7) ON CONFLICT DO NOTHING`,
        [
          locationKey,
          mapping.country,
          mapping.city,
          mapping.lat_min,
          mapping.lat_max,
          mapping.lng_min,
          mapping.lng_max
        ]
      );
    }

    await client.query('COMMIT');

    console.log('✅ Location data populated successfully!');

    // Verify the data
    const locationCount = await client.query('SELECT COUNT(*) as count FROM locations');
    const mappingCount = await client.query('SELECT COUNT(*) as count FROM coordinate_mappings');

    console.log(`📊 Inserted ${locationCount.rows[0].count} location records`);
    console.log(`📊 Inserted ${mappingCount.rows[0].count} coordinate mapping records`);

  } catch (error) {
    await client.query('ROLLBACK');
    console.error('❌ Error populating location data:', error);
    throw error;
  } finally {
    client.release();
    await pool.end();
  }
}

// Run the population script
populateLocations().catch(console.error);
